/// Represents the grammatical genders used for inflection in various languages.
///
/// This enum provides a type-safe way to specify gender when using the
/// grammar engine to generate correctly inflected strings.
enum GrammaticalGender {
  /// Represents masculine grammatical gender.
  masculine,

  /// Represents feminine grammatical gender.
  feminine,

  /// Represents neutral grammatical gender.
  neutral,
}
